package lecture04;

public class MathClassDemoProgram {

	public static void main(String[] args) {

		double x1 = Math.cos(Math.PI / 2);
		double x2 = Math.log(Math.pow(Math.E, 2));
		double x3 = Math.atan2(-1, 1) + Math.PI / 4;

		System.out.println("x1: " + x1);
		System.out.println("x2: " + x2);
		System.out.println("x3: " + x3);
	}
	
}
